/* ================================================================
 * 设计目标：
 *   1. 每个观察者持有一个独立内存池，所有分析过程中创建的对象从池内分配
 *   2. 分配走 bump-pointer（~5ns），无锁，零系统调用
 *   3. 观察者销毁时整池释放，不再需要回收站和入回收站的三阶段清理
 *   4. 引用计数保留（弱引用安全），但解引用归零时不调 free()
 *
 * 与现有代码的集成点（未来修改 chan.c/chan.h 时参考）：
 *   - 对象头 增加 内存池* 所属内存池 字段
 *   - 分配() 优先走 全局内存池，回退到 calloc
 *   - 解引用() 归零时调 对象销毁() 但不调 free()（除非 所属内存池==NULL）
 *   - 观察者_新建() 初始化内存池，替代 回收站 初始化
 *   - 释放观察者() 调 内存池_释放()，删除回收站和入回收站 相关逻辑
 *   - 删除 chan_回收站 全局变量和 入回收站() 函数
 */

/* ================================================================
 *  内存池块 — 内存池的组成单元
 * ================================================================ */

/* 对齐要求：16 字节（覆盖指针、double 和 SSE 类型） */
#define 内存池_对齐 16

/* 内存池块头部对齐后的大小 */
#define 内存池_头部大小 \
((sizeof(内存池块) + 内存池_对齐 - 1) & ~((size_t)(内存池_对齐 - 1)))

typedef struct 内存池块 {
    struct 内存池块 *下一块; /* 链表下一节点        */
    size_t 容量; /* 本块数据区总容量     */
    size_t 已用; /* 本块数据区已用字节   */
    /* char 数据[容量] — 柔性数组，实际内存紧跟头部之后 */
} 内存池块;

/* ================================================================
 *  内存池 — 观察者持有
 * ================================================================ */

typedef struct {
    内存池块 *首块; /* 链表头               */
    内存池块 *当前块; /* 当前 bump-pointer 块  */
    size_t 块大小; /* 下一个块的默认大小    */
    size_t 总容量; /* 所有块的总数据容量    */
    size_t 总分配; /* 累计分配字节数        */
    size_t 分配次数; /* 累计分配调用次数      */
    void *清理头; /* 侵入式清理链表头：分配时串联，释放观察者时遍历清理 */
    pthread_mutex_t 锁; /* 保护 bump-pointer 分配和块链表操作 */
} 内存池;

/* ================================================================
 *  API
 * ================================================================ */

/* 初始化内存池，预分配第一块。
 *
 * 初始块大小参数：
 *   - 64 KB:  小型数据（< 5000 根K线）
 *   - 1 MB:   中等数据（~10000 根K线）
 *   - 16 MB:  大型数据（~50000 根K线，推荐）
 *   - 0:      使用默认值（1 MB）                         */
void 内存池_初始化(内存池 *self, size_t 初始块大小);

/* 从内存池分配 size 字节，返回零初始化的内存。
 *
 * 行为：
 *   - 当前块剩余空间足够 → bump pointer，两条指令
 *   - 剩余不足 → 分配新块（默认 2x 扩容），bump from new block
 *   - 单次请求 > 块大小 → 分配 oversized 专用块
 *
 * 返回 NULL 表示内存耗尽（仅当 malloc 失败时）。 */
void *内存池_分配(内存池 *self, size_t 大小);

/* 分配并拷贝（用于 strdup 等场景） */
void *内存池_分配拷贝(内存池 *self, const void *源, size_t 大小);

/* 重置：把所有块的已用指针归零。
 * 底层内存不释放，后续分配从第一块开始覆盖。
 * 用于同一观察者进行多次独立分析。 */
void 内存池_重置(内存池 *self);

/* 释放所有底层内存块。
 * 调用后内存池不可再使用（除非重新初始化）。 */
void 内存池_释放(内存池 *self);

/* 调试信息 */
size_t 内存池_使用量(const 内存池 *self);

size_t 内存池_总容量(const 内存池 *self);

size_t 内存池_分配次数(const 内存池 *self);

size_t 内存池_块数量(const 内存池 *self);

